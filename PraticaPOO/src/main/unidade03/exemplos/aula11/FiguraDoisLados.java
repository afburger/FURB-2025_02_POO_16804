package unidade03.exemplos.aula11;

public abstract class FiguraDoisLados extends Figura {

	private int ladoA;
	private int ladoB;

	public int getLadoA() {
		return ladoA;
	}

	public int getLadoB() {
		return ladoB;
	}

	public void setLadoA(int ladoA) {
		this.ladoA = ladoA;
	}

	public void setLadoB(int ladoB) {
		this.ladoB = ladoB;
	}

}
